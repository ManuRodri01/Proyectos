        private void initContent(){
            
            this.setVisible(true);
            
            listPanel = new ListsPanel(this.currentUser, this.logicController);
            listPanel.setSize(776, 668);
            listPanel.setLocation(0,0);
            profilePanel = new ProfilePanel(logicController, currentUser, this);
            profilePanel.setPreferredSize(new Dimension(857,690));
            profilePanel.setLocation(0, 0);
            
            
            Username.setText(currentUser.getUsername());
            if(currentUser.existImage()){
                ImageIcon profilePicture = new ImageIcon(currentUser.getImage());
                Image profileImage = profilePicture.getImage().getScaledInstance(60,60,Image.SCALE_SMOOTH);
                ImageIcon scaledProfilePicture = new ImageIcon(profileImage);
                ProfilePicture.setIcon(scaledProfilePicture);
                jPanel1.revalidate();
                jPanel1.repaint();
            }
            this.setIconImage(new ImageIcon(this.getClass().getResource("/icons/LogoNoText.png")).getImage());
            
            PrincipalPanel.removeAll();
            PrincipalPanel.add(listPanel, BorderLayout.CENTER);
            listPanel.createAllLists();
            PrincipalPanel.revalidate();
            PrincipalPanel.repaint();
        }
        
        public void changePicture(String path){
            ImageIcon profilePicture = new ImageIcon(path);
            Image profileImage = profilePicture.getImage().getScaledInstance(60,60,Image.SCALE_SMOOTH);
            ImageIcon scaledProfilePicture = new ImageIcon(profileImage);
            ProfilePicture.setIcon(scaledProfilePicture);
            jPanel1.revalidate();
            jPanel1.repaint();
        }
        
        public void exitToLogin(){
            dispose();
            new Login(this.logicController);
        }

